package com.example.rjdtask1.model;

public class PK17AbdData {
    private String num;
    private String sob;
    private String b_ind;

    public PK17AbdData(String num, String sob, String b_ind) {
        this.num = num;
        this.sob = sob;
        this.b_ind = b_ind;
    }

    public String getNum() {
        return num;
    }

    public String getSob() {
        return sob;
    }

    public String getB_ind() {
        return b_ind;
    }
}
